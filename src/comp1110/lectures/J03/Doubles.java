package comp1110.lectures.J03;

public class Doubles {
    public static void main(String[] args) {
        double x = 3.1;
        System.out.println("x: "+x);
        double y = 1.23456e-7;
        System.out.println("y: "+y);
        String s = "3.123";
        double z = Double.parseDouble(s);
        System.out.println("z: "+z);
    }

}
